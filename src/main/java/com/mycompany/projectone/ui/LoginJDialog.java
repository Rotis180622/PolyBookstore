   @Override
    public void validateForm() {
        String username = txtUser.getText().trim();
        String password = txtPass.getText().trim();

        // 1. Kiểm tra người dùng có nhập hay không
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ tên đăng nhập và mật khẩu!");
            txtUser.setText("");
            txtPass.setText("");
            txtUser.requestFocus();
            return;
        }

        // 2. Tìm tài khoản khớp
        accounts = dao.findAll();
        for (Account account : accounts) {
            if (username.equals(account.getUsername()) &&
                password.equals(account.getPassword())) {

                // 3. Đúng tài khoản → đóng login → mở welcome → mở main
                this.dispose();

                WelcomeJDialog welcome = new WelcomeJDialog((javax.swing.JFrame) this.getParent(), true);
                welcome.setVisible(true);

                if (username.toLowerCase().startsWith("quanly")) {
                new New_MainUI().setVisible(true);
                } else if (username.toLowerCase().startsWith("nhanvien")) {
                    new MainUI_Employee().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Tài khoản không xác định quyền!");
                }

                return; // Thoát luôn hàm
            }
        }

        // 4. Sai tài khoản → báo lỗi, xoá nhập
        JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu!");
        txtUser.setText("");
        txtPass.setText("");
        txtUser.requestFocus();
    }



