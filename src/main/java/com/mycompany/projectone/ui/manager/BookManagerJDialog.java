    @Override
    public void fillToTable() {
        model = (DefaultTableModel) tblBooks.getModel();
        model.setRowCount(0);

        List<Book> books = dao.findAll();
        for (Book book : books) {
            Object[] rowData = {
                book.getBookID(),
                book.getTitle(),
                book.getAuthor(),
                book.getPublisherYear(),
                book.getPrice(),
                book.getCategoryID(),
                book.getSupplierID(),
                book.getQuantityInStock()
            };
            model.addRow(rowData);
        }
        clear();
    }

    @Override
    public void edit() {
        int index = tblBooks.getSelectedRow();
        if (index >= 0) {
            Book book = dao.findAll().get(index);
            setForm(book);
            tabs.setSelectedIndex(1);
            setEditable(true);
        }
    }

