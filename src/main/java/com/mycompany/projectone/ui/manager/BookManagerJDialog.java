    @Override
    public void setForm(Book book) {
        txtBookID.setText(book.getBookID() != 0 ? String.valueOf(book.getBookID()) : "");
        txtTitle.setText(book.getTitle() != null ? book.getTitle() : "");
        txtAuthor.setText(book.getAuthor() != null ? book.getAuthor() : "");
        txtPublisherYear.setText(book.getPublisherYear() != 0 ? String.valueOf(book.getPublisherYear()) : "");
        txtPrice.setText(book.getPrice() != null ? book.getPrice().toPlainString() : "");
        txtCategoriesID.setText(String.valueOf(book.getCategoryID()));
        txtSupplierID.setText(String.valueOf(book.getSupplierID()));
        txtQuantityInStock.setText(book.getQuantityInStock() != 0 ? String.valueOf(book.getQuantityInStock()) : "");
    }
    
    @Override
    public Book getForm() {
        Book book = new Book();
        if (!txtBookID.getText().trim().isEmpty()) {
            book.setBookID(Integer.parseInt(txtBookID.getText().trim()));
        }
        book.setTitle(txtTitle.getText().trim());
        book.setAuthor(txtAuthor.getText().trim());
        book.setPublisherYear(Integer.valueOf(txtPublisherYear.getText().trim()));
        try {
            book.setPrice(new BigDecimal(txtSupplierID.getText().trim()));
        } catch (NumberFormatException e) {
            book.setPrice(BigDecimal.ZERO);
        }
            book.setCategoryID(Integer.valueOf(txtCategoriesID.getText()));


            book.setSupplierID(Integer.valueOf(txtSupplierID.getText()));


        try {
            book.setQuantityInStock(Integer.parseInt(txtQuantityInStock.getText().trim()));
        } catch (NumberFormatException e) {
            book.setQuantityInStock(0);
        }

        return book;
    }

    @Override
    public void create() {
        Book book = getForm();
        dao.create(book);
        fillToTable();
        clear();
    }

    @Override
    public void update() {
        Book book = getForm();
        dao.update(book);
        XDialog.alert("Cập nhật thành công!");
        fillToTable();
    }

    @Override
    public void delete() {
        if (XDialog.confirm("Bạn muốn xóa mục đã chọn?")) {
            try {
                Integer id = Integer.valueOf(txtBookID.getText().trim());
                dao.deleteById(id);
                fillToTable();
                clear();
            } catch (NumberFormatException e) {
                XDialog.alert("Mã sách không hợp lệ!");
            }
        }
    }

