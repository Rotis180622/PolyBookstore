    @Override
    public void findByID() {
        model = (DefaultTableModel) tblBooks.getModel();
        model.setRowCount(0);

        String input = txtKeyword.getText().trim();
        if (input.isEmpty()) {
            XDialog.alert("Vui lòng nhập mã sách!");
            return;
        }

        try {
            int bookID = Integer.parseInt(input);
            Book book = dao.findById(bookID);
            if (book != null) {
                Object[] rowData = {
                    book.getBookID(),
                    book.getTitle(),
                    book.getAuthor(),
                    book.getPublisherYear(),
                    book.getPrice(),
                    book.getCategoryID(),
                    book.getSupplierID(),
                    book.getQuantityInStock()
                };
                model.addRow(rowData);
            } else {
                XDialog.alert("Không tìm thấy sách với mã: " + input);
            }
        } catch (NumberFormatException e) {
            XDialog.alert("Mã sách phải là số nguyên!");
        }
        txtKeyword.setText("");
    }
    
