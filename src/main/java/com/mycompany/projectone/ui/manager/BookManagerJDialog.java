    @Override
    public void moveTo(int rowIndex) {
        if (rowIndex < 0) {
            moveLast();
        } else if (rowIndex >= tblBooks.getRowCount()) {
            moveFirst();
        } else {
            tblBooks.clearSelection();
            tblBooks.setRowSelectionInterval(rowIndex, rowIndex);
            edit();
        }
    }

