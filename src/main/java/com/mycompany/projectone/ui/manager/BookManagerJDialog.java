    public void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblBooks.getRowCount(); i++) {
            tblBooks.setValueAt(checked, i, tblBooks.getColumnCount() - 1); // Cột cuối là checkbox
        }
    }

    public void setChecked(boolean checked) {
        tblBooks.setValueAt(checked, tblBooks.getSelectedRow(), tblBooks.getColumnCount() - 1);
    }

    @Override
    public void deleteCheckedItems() {
        if (XDialog.confirm("Bạn có chắc chắn xóa?")) {
            for (int i = 0; i < tblBooks.getRowCount(); i++) {
                Boolean checked = (Boolean) tblBooks.getValueAt(i, tblBooks.getColumnCount() - 1);
                if (checked != null && checked) {
                    Book book = books.get(i);
                    dao.deleteById(book.getBookID());
                }
            }
            fillToTable();
        }
    }

