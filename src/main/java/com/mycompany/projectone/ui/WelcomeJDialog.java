    @Override
    public void waiting() {
        this.setLocationRelativeTo(null); // Canh giữa màn hình

        new Thread(() -> {
            for (int i = 0; i <= 100; i++) {
                progressBar.setValue(i);

                try {
                    Thread.sleep(10); // 30ms mỗi bước -> tổng khoảng 3 giây
                } catch (InterruptedException ex) {
                    Logger.getLogger(WelcomeJDialog.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            this.dispose(); // Đóng dialog sau khi loading xong
        }).start();
    }

